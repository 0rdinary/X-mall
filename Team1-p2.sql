FROM (  SELECT User_id
	FROM CUSTOMER NATURAL JOIN ORDERED_BY
	WHERE Is_ordered = TRUE
	AND EXISTS (SELECT C.User_id
		    FROM CUSTOMER C
	            WHERE NOT EXISTS (SELECT T.User_id
		    FROM (SELECT * FROM SHOPPINGBAG NATURAL JOIN ORDERED_BY) T
		    WHERE T.Is_ordered = false
		    AND C.User_id = T.User_id))) TU,
		    (SELECT User_id, Bag_idx
		    FROM ORDERED_BY NATURAL JOIN SHOPPINGBAG
		    WHERE Is_ordered = TRUE) TEMP NATURAL JOIN INCLUDE NATURAL JOIN ITEM
WHERE TU.User_id = TEMP.User_id
GROUP BY Item_id
ORDER BY Ordered_count DESC
LIMIT 5;

