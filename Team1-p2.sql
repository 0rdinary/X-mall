SELECT DISTINCT User_id
FROM (
	SELECT ITEM.Item_id, ITEM.Name, ITEM.CID, STOCK*ITEM.Price AS TOTAL_RPICE
	FROM (
		SELECT TEMP.Item_id, ITEM.Name, ITEM.CID, SUM(TEMP.Stock) AS STOCK
		FROM (SELECT * FROM ORDERED_BY NATURAL JOIN INCLUDE) TEMP, ITEM
		WHERE TEMP.Is_ordered = TRUE
		AND TEMP.Item_id = ITEM.Item_id
		GROUP BY TEMP.Item_id
		ORDER BY ITEM.Item_id ASC ) TEMP,
		ITEM
	WHERE ITEM.Item_id = TEMP.Item_id
	ORDER BY STOCK*ITEM.Price DESC
	LIMIT 1) BS, 
	(SELECT *
	FROM (SELECT * FROM SHOPPINGBAG NATURAL JOIN INCLUDE) SP NATURAL JOIN ITEM) CS
WHERE BS.CID = CS.CID
OR BS.Item_id = CS.Item_id
ORDER BY User_id ASC;
