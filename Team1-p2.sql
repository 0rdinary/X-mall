SET foreign_key_checks = 0;
DROP TABLE ITEM;
CREATE TABLE ITEM(
  Item_id       INT          NOT NULL,
  Name          VARCHAR(50)  NOT NULL,
  Price         INT          NOT NULL,
  Discount_rate INT,
  Exp_date      DATE,                  #유통기한
  Rating        DECIMAL(2,1),
  Importer      VARCHAR(30),
  CID           INT,
  PID           INT,
  PRIMARY KEY (Item_id)
);

DROP TABLE CATEGORY;
CREATE TABLE CATEGORY(
  CID            INT         NOT NULL,
  Large_category VARCHAR(20) NOT NULL,
  Small_category VARCHAR(20) NOT NULL,
  PRIMARY KEY (CID)
);

DROP TABLE PRODUCERLOCATION;
CREATE TABLE PRODUCERLOCATION(
  PID      INT NOT NULL,
  Producer VARCHAR(20),
  Location VARCHAR(20) NOT NULL,
  PRIMARY KEY(PID)
);

DROP TABLE RETAILER;
CREATE TABLE RETAILER(
  Store_idx INT         NOT NULL,
  Location  VARCHAR(20) NOT NULL,
  PRIMARY KEY (Store_idx)
);

DROP TABLE CUSTOMER;
CREATE TABLE CUSTOMER(
  User_id       INT         NOT NULL,
  User_password VARCHAR(10) NOT NULL,
  Address       VARCHAR(30) NOT NULL,
  Phone_number  VARCHAR(20) NOT NULL,
  Membership    INT,
  Sex           CHAR(1),
  Age		INT,
  Job           VARCHAR(20),
  PRIMARY KEY (User_id)
);

DROP TABLE SHOPPINGBAG;
CREATE TABLE SHOPPINGBAG(
  User_id     INT NOT NULL,
  Bag_idx     INT NOT NULL,
  Total_price INT,
  Bag_name    VARCHAR(30),
  PRIMARY KEY (User_id, Bag_idx),
  FOREIGN KEY (User_id) REFERENCES CUSTOMER(User_id)
);

ALTER TABLE ITEM
ADD FOREIGN KEY (CID)
REFERENCES CATEGORY (CID);

ALTER TABLE ITEM
ADD FOREIGN KEY (PID)
REFERENCES PRODUCERLOCATION (PID);

DROP TABLE MANAGE;
CREATE TABLE MANAGE(
  Store_idx INT         NOT NULL,
  Location  VARCHAR(30) NOT NULL,
  Item_id   INT         NOT NULL,
  Stock     INT,
  PRIMARY KEY (Store_idx, Item_id),
  FOREIGN KEY (Store_idx) REFERENCES RETAILER(Store_idx),
  FOREIGN KEY (Item_id)   REFERENCES ITEM(Item_id)
);

DROP TABLE INCLUDE;
CREATE TABLE INCLUDE(
  Bag_idx INT NOT NULL,
  User_id INT NOT NULL,
  Item_id INT NOT NULL,
  Stock   INT,
  PRIMARY KEY (User_id, Bag_idx, Item_id),
  FOREIGN KEY (User_id, Bag_idx) REFERENCES SHOPPINGBAG(User_id, Bag_idx),
  FOREIGN KEY (Item_id) REFERENCES ITEM(Item_id)
);

DROP TABLE ORDERED_BY;
CREATE TABLE ORDERED_BY(
  User_id      INT            NOT NULL,
  Bag_idx      INT            NOT NULL,
  Is_ordered   TINYINT(1)     NOT NULL,
  When_ordered Date,
  Store_idx    INT,
  PRIMARY KEY (User_id, Bag_idx),
  FOREIGN KEY (User_id, Bag_idx) REFERENCES SHOPPINGBAG(User_id, Bag_idx),
  FOREIGN KEY (Store_idx) REFERENCES RETAILER(Store_idx)
);


